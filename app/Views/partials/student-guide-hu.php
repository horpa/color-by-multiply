<h1>Hogyan használd a munkalapot</h1>

<p>Ez az útmutató <strong>tanulóknak</strong> szól. Segít használni a tanárod által adott papírt.</p>

<p>A lapodon egy <strong>színes négyzetekből álló kép</strong> és alatta <strong>matek feladatok</strong> lesznek.</p>

<h2>Mi van a lapon?</h2>

<p>A munkalapon három rész van:</p>

<ol>
    <li><strong>A nagy rács</strong> — egy kép színes és fehér négyzetekkel.</li>
    <li><strong>A színtáblázat</strong> — kis színes körök számokkal.</li>
    <li><strong>A feladatok</strong> — megoldandó matek feladatok.</li>
</ol>

<h2>Kék és zöld számok</h2>

<p>Nézd meg a rácsot:</p>

<ul>
    <li>A <strong>bal oldali kék számok</strong> a <strong>sort</strong> mutatják (melyik sorban van a négyzet).</li>
    <li>A <strong>felső zöld számok</strong> az <strong>oszlopot</strong> mutatják (melyik oszlopban van a négyzet).</li>
</ul>

<pre class="guide-diagram" aria-hidden="true"><code>        1   2   3   4   ...   (zöld = oszlopok)
      ┌───┬───┬───┬───┐
  1   │   │   │ ■ │   │       (kék = sorok)
  2   │   │ ■ │   │   │
  3   │ ■ │   │   │   │
      └───┴───┴───┴───┘</code></pre>

<p>Egy színes négyzet megtalálásához:</p>

<ol>
    <li>Nézd meg a <strong>felső zöld számot</strong> → ez az <strong>oszlop</strong>.</li>
    <li>Nézd meg a <strong>bal oldali kék számot</strong> → ez a <strong>sor</strong>.</li>
</ol>

<p>A <strong>fehér négyzetek</strong> üres háttér. Ezekhez nincs feladat.</p>

<h2>A színtáblázat</h2>

<p>A rács alatt színes köröket látsz számokkal (1, 2, 3…).</p>

<p>Minden színnek van egy száma a képen. Ugyanaz a szín megjelenik a feladatok mellett is, hogy tudd, <strong>melyik négyzethez</strong> tartozik a feladat.</p>

<h2>A feladatok</h2>

<p>Minden feladat egy kis matek feladat. Lehet benne <strong>×</strong> (szorzás) vagy <strong>÷</strong> (osztás).</p>

<p>Néhány szám már meg van adva. Az egyik helyen egy <strong>üres mező</strong> van — oda <strong>neked</strong> kell beírni a választ:</p>

<table class="guide-table">
    <thead>
        <tr>
            <th>Mező színe</th>
            <th>Mit írj be</th>
        </tr>
    </thead>
    <tbody>
        <tr>
            <td><strong>Világoskék mező</strong> sötétkék kerettel</td>
            <td>A <strong>sor</strong> számát</td>
        </tr>
        <tr>
            <td><strong>Világoszöld mező</strong> sötétzöld kerettel</td>
            <td>Az <strong>oszlop</strong> számát</td>
        </tr>
    </tbody>
</table>

<p>A <span class="exercise-blue">kék</span> számok sor számok. A <span class="exercise-green">zöld</span> számok oszlop számok.</p>

<h3>Példák</h3>

<p><strong>Szorzás</strong></p>

<ul>
    <li>
        <span class="exercise-blank exercise-blank--row" aria-hidden="true"></span>
        <span>× 4 = 12</span>
        → a <strong>sor</strong> számát írd a kék mezőbe.
    </li>
    <li>
        <span>3 × </span>
        <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
        <span>= 12</span>
        → az <strong>oszlop</strong> számát írd a zöld mezőbe.
    </li>
</ul>

<p><strong>Osztás</strong></p>

<ul>
    <li>
        <span>12 ÷ </span>
        <span class="exercise-blank exercise-blank--row" aria-hidden="true"></span>
        <span>= 4</span>
        → a <strong>sor</strong> számát írd a kék mezőbe.
    </li>
    <li>
        <span>12 ÷ 3 = </span>
        <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
        → az <strong>oszlop</strong> számát írd a zöld mezőbe.
    </li>
</ul>

<h2>Egy feladat megoldása — lépésről lépésre</h2>

<p>Próbáld így:</p>

<ol>
    <li>Nézd meg a feladat végén a <strong>kis színes kört</strong>. Jegyezd meg a színét és a számát.</li>
    <li>Keress a nagy rácson egy négyzetet <strong>ugyanolyan színnel</strong>.</li>
    <li>Olvasd le a négyzet <strong>kék sor</strong> és <strong>zöld oszlop</strong> számát.</li>
    <li>Nézd meg a feladatot. Az üres mező <strong>kék</strong> vagy <strong>zöld</strong>?
        <ul>
            <li><strong>Kék mező</strong> → a <strong>sor</strong> számát írd be.</li>
            <li><strong>Zöld mező</strong> → az <strong>oszlop</strong> számát írd be.</li>
        </ul>
    </li>
    <li>Írd a számot <strong>szépen a mezőbe</strong>.</li>
</ol>

<h3>Példa</h3>

<p>
    Feladat:
    <span class="exercise-blue">3</span>
    <span>× </span>
    <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
    <span>= 12</span>,
    mellette piros színjelölő.
</p>

<ul>
    <li>A piros négyzet a <strong>3. sorban</strong> és a <strong>4. oszlopban</strong> van.</li>
    <li>Az üres mező <strong>zöld</strong>, ezért az <strong>oszlop</strong> számát írod be: <strong>4</strong>.</li>
</ul>

<p>Ellenőrzés: 3 × 4 = 12. Jó!</p>

<h2>Tippek</h2>

<ul>
    <li>Egy számot írj jól olvashatóan minden mezőbe.</li>
    <li>A színjelölő segít megtalálni a megfelelő négyzetet a rácson.</li>
    <li>A kék mindig a <strong>sort</strong>, a zöld mindig az <strong>oszlopot</strong> jelenti.</li>
    <li>Ne siess — figyelmesen olvasd le a sort és az oszlopot.</li>
</ul>

