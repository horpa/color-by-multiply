<h1>How to Use Your Worksheet</h1>

<p>This guide is for <strong>students</strong>. It helps you use the paper your teacher gives you.</p>

<p>You will see a <strong>picture made of colored squares</strong> and a list of <strong>math exercises</strong> below it.</p>

<h2>What is on the paper?</h2>

<p>Your worksheet has three parts:</p>

<ol>
    <li><strong>The big grid</strong> — a picture with colored and white squares.</li>
    <li><strong>The color key</strong> — small colored circles with numbers.</li>
    <li><strong>The exercises</strong> — math problems to solve.</li>
</ol>

<h2>Blue and green numbers</h2>

<p>Look at the grid:</p>

<ul>
    <li><strong>Blue numbers</strong> on the <strong>left</strong> tell you the <strong>row</strong> (which row a square is in).</li>
    <li><strong>Green numbers</strong> on the <strong>top</strong> tell you the <strong>column</strong> (which column a square is in).</li>
</ul>

<pre class="guide-diagram" aria-hidden="true"><code>        1   2   3   4   ...   (green = columns)
      ┌───┬───┬───┬───┐
  1   │   │   │ ■ │   │       (blue = rows)
  2   │   │ ■ │   │   │
  3   │ ■ │   │   │   │
      └───┴───┴───┴───┘</code></pre>

<p>To find a colored square:</p>

<ol>
    <li>Look <strong>across</strong> to the green number on top → that is the <strong>column</strong>.</li>
    <li>Look <strong>down</strong> to the blue number on the left → that is the <strong>row</strong>.</li>
</ol>

<p><strong>White squares</strong> are empty background. You do not need math problems for those.</p>

<h2>The color key</h2>

<p>Below the grid you see colored circles with numbers (1, 2, 3…).</p>

<p>Each color on the picture has a number. The same colors appear next to the exercises so you know <strong>which square</strong> each problem belongs to.</p>

<h2>The exercises</h2>

<p>Each exercise is a small math problem. You may see <strong>×</strong> (multiply) or <strong>÷</strong> (divide).</p>

<p>Some numbers are already written. One spot is an <strong>empty box</strong> where <strong>you</strong> write the answer:</p>

<table class="guide-table">
    <thead>
        <tr>
            <th>Box color</th>
            <th>What to write</th>
        </tr>
    </thead>
    <tbody>
        <tr>
            <td><strong>Light blue box</strong> with dark blue edge</td>
            <td>Write the <strong>row</strong> number</td>
        </tr>
        <tr>
            <td><strong>Light green box</strong> with dark green edge</td>
            <td>Write the <strong>column</strong> number</td>
        </tr>
    </tbody>
</table>

<p>Numbers written in <span class="exercise-blue">blue</span> are row numbers. Numbers written in <span class="exercise-green">green</span> are column numbers.</p>

<h3>Examples</h3>

<p><strong>Multiplication</strong></p>

<ul>
    <li>
        <span class="exercise-blank exercise-blank--row" aria-hidden="true"></span>
        <span>× 4 = 12</span>
        → write the <strong>row</strong> number in the blue box.
    </li>
    <li>
        <span>3 × </span>
        <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
        <span>= 12</span>
        → write the <strong>column</strong> number in the green box.
    </li>
</ul>

<p><strong>Division</strong></p>

<ul>
    <li>
        <span>12 ÷ </span>
        <span class="exercise-blank exercise-blank--row" aria-hidden="true"></span>
        <span>= 4</span>
        → write the <strong>row</strong> number in the blue box.
    </li>
    <li>
        <span>12 ÷ 3 = </span>
        <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
        → write the <strong>column</strong> number in the green box.
    </li>
</ul>

<h2>How to solve one exercise — step by step</h2>

<p>Try this:</p>

<ol>
    <li>Look at the <strong>small colored circle</strong> at the end of the exercise. Remember its color and number.</li>
    <li>Find a square on the big grid that has <strong>the same color</strong>.</li>
    <li>Read the <strong>blue row number</strong> and the <strong>green column number</strong> for that square.</li>
    <li>Look at the exercise. Is the empty box <strong>blue</strong> or <strong>green</strong>?
        <ul>
            <li><strong>Blue box</strong> → write the <strong>row</strong> number.</li>
            <li><strong>Green box</strong> → write the <strong>column</strong> number.</li>
        </ul>
    </li>
    <li>Write your number <strong>neatly inside the box</strong>.</li>
</ol>

<h3>Example</h3>

<p>
    Exercise:
    <span class="exercise-blue">3</span>
    <span>× </span>
    <span class="exercise-blank exercise-blank--column" aria-hidden="true"></span>
    <span>= 12</span>
    with a red color chip.
</p>

<ul>
    <li>The red square is in <strong>row 3</strong> and <strong>column 4</strong>.</li>
    <li>The empty box is <strong>green</strong>, so you write the <strong>column</strong> number: <strong>4</strong>.</li>
</ul>

<p>Check: 3 × 4 = 12. Correct!</p>

<h2>Tips</h2>

<ul>
    <li>Write one digit clearly inside each box.</li>
    <li>Use the color chip to find the right square on the grid.</li>
    <li>Blue always means <strong>row</strong>, green always means <strong>column</strong>.</li>
    <li>Take your time — read the row and column carefully.</li>
</ul>

